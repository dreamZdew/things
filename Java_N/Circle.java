public class Circle {
    static double PI = 3.1416;
    public static void main(String[] args) {
        int r1 = 2;
        double r2 = 3.7, r3 = 5.12;
        System.out.println("area r1=" + (PI * r1 * r1));
        System.out.println("area r2=" + (PI * r2 * r2));
        System.out.println("area r3=" + (PI * r3 * r3));
    }
}
